Temperatura = float(input("Digite a temperatura em Celsius: "))
if Temperatura < 11:
    print("A temperatura esta gelada🥶🧊")
elif 11 <= Temperatura < 20:
    print("A temperatura esta normal😁😄")
elif 20 <= Temperatura > 30:
    print("A temperatura esta quente🔥🥵")

else:
    print("Obrigado por usar o programa! ate mais!👋")
    